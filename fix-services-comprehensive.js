let totalFixes = 0;
let filesProcessed = 0;

files.forEach(filename => {
    let filePath = filename;
    
    if (!fs.existsSync(filePath)) {
        console.log(`✗ ${filename} not found`);
        return;
    }
    
    let buffer = fs.readFileSync(filePath);
    const originalSize = buffer.length;
    let fixes = 0;
    
    replacements.forEach(({ pattern, replacement, emoji }) => {
        let index;
        while ((index = buffer.indexOf(pattern)) !== -1) {
            const newBuffer = Buffer.alloc(buffer.length - pattern.length + replacement.length);
            buffer.copy(newBuffer, 0, 0, index);
            replacement.copy(newBuffer, index);
            buffer.copy(newBuffer, index + replacement.length, index + pattern.length);
            buffer = newBuffer;
            fixes++;
        }
    });
    
    if (fixes > 0) {
        fs.writeFileSync(filePath, buffer);
        totalFixes += fixes;
        filesProcessed++;
        console.log(`✓ ${filename.padEnd(30)} ${String(fixes).padStart(2)} fixes (${originalSize} → ${buffer.length} bytes)`);
    } else {
        console.log(`  ${filename.padEnd(30)} 0 fixes`);
    }
});

console.log(`\n✅ Processed: ${filesProcessed} files | Total emoji fixes: ${totalFixes}`);
